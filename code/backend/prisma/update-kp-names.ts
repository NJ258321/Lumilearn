async function main() {
  console.log('========================================')
  console.log('更新知识点名称...')
  console.log('========================================\n')

  for (const [courseId, chapterNames] of Object.entries(knowledgePointNames)) {
    // 获取该课程的所有章节
    const chapters = await prisma.chapter.findMany({
      where: { courseId },
      orderBy: { order: 'asc' }
    })

    for (let i = 0; i < chapters.length; i++) {
      const chapter = chapters[i]
      const kpNames = chapterNames[i] || []

      // 获取该章节的所有知识点
      const knowledgePoints = await prisma.knowledgePoint.findMany({
        where: { chapterId: chapter.id },
        orderBy: { id: 'asc' }
      })

      // 更新知识点名称
      for (let j = 0; j < knowledgePoints.length; j++) {
        const kp = knowledgePoints[j]
        const newName = kpNames[j] || kp.name

        await prisma.knowledgePoint.update({
          where: { id: kp.id },
          data: { name: newName }
        })
      }
    }
    console.log(`✓ 更新课程 ${courseId.slice(-6)} 的知识点名称`)
  }

  // 验证
  console.log('\n========================================')
  console.log('验证更新结果：')
  const courses = await prisma.course.findMany({
    include: {
      chapters: {
        orderBy: { order: 'asc' },
        include: {
          knowledgePoints: { take: 2 }
        }
      }
    }
  })

  for (const c of courses) {
    console.log(`\n${c.name}:`)
    for (const ch of c.chapters) {
      const names = ch.knowledgePoints.map(kp => kp.name).join(', ')
      console.log(`  ${ch.name}: ${names}...`)
    }
  }
}

main().catch(console.error).finally(() => prisma.$disconnect())
